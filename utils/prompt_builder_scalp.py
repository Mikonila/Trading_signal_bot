from datetime import datetime

def build_scalp_prompt(candles_by_tf: dict) -> str:
    now = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ")

    def format_candles(tf: str):
        candles = candles_by_tf.get(tf, [])
        text = f"⏱ Таймфрейм {tf} ({len(candles)} свечей):\n"
        for c in candles:
            text += f"{c['time']}: O={c['open']} H={c['high']} L={c['low']} C={c['close']}\n"
        return text

    candle_text = "\n".join([format_candles(tf) for tf in candles_by_tf])

    prompt = f"""
Ты — опытный трейдер-скальпер. Анализируй BTC/USDT по графикам на трех данных тебе таймфреймах.
Используй свечи 1m, 15m и 1h. Учитывай глобальный тренд (по H1) и локальный вход (по M1).
Определи тренд:
H1 — глобальный тренд.
M15 — локальное подтверждение.
M1 — точка входа.
Вход возможен только если тренды H1 и M15 совпадают.

🔹 Условия входа:
LONG: цена выше EMA, структура HH, подтверждение RSI/MACD.
SHORT: цена ниже EMA, структура LL, подтверждение RSI/MACD.
NO TRADE: если нет совпадения сигналов или рынок во флэте (M15 < 0.2%).

🔹 Подсказки:
EMA50 > EMA200 → восходящий тренд.
EMA50 < EMA200 → нисходящий тренд.
RSI > 70 → не открывать LONG (перекупленность).
RSI < 30 → не открывать SHORT (перепроданность).
MACD и Signal должны подтверждать тренд, а не быть единственным критерием.
SL: чуть ниже/выше EMA50 или локального минимума/максимума.
TP: 0.5–1% или по ближайшему уровню сопротивления/поддержки.
Если импульс сильный (MACD Histogram растёт) → держать 30–40 мин.
Если сигнал средний → держать 10–15 мин.
Если сигнал слабый → не входить.
Если график во флэте (M15 < 0.2%) → не входить.
Тейк-профит должен быть не менее 2х стоп-лосса (RR ≥ 2) и не менее 0.7% от цены. Если не выполняется — не входить.

🔹 Уверенность (%) рассчитывать по совпадению индикаторов:
Например:
3 подтверждения (EMA, RSI, MACD) → 80–90%.
2 подтверждения → 60–70%.
1 подтверждение → сигнал не выдаётся.


🔹 Стратегия: быстрые сделки (до 30 минут)
Используй EMA, RSI, MACD и структуру HH/LL.

🔹 Формат вывода строго в HTML (только поддерживаемые Telegram теги: <b>, <i>, <code>, <a>). Пример вывода:

<b>Сигнал:</b> Покупать/Продавать/Держать
<b>Цена BTC/USDT:</b> 115200   
<b>SL:</b> 114800 / <b>TP:</b> 115900  
<b>Комментарий:</b> На M1 и H1 наблюдается восходящий тренд. EMA и MACD подтверждают бычью динамику.  
<b>Держать:</b> 20 минут  
<b>Уверенность:</b> 75%

❗️Заголовки должны быть точно такими же:
- <b>Сигнал:</b>  
- <b>Цена BTC/USDT:</b>  
- <b>SL:</b> … / <b>TP:</b> …  
- <b>Комментарий:</b>  
- <b>Держать:</b>  
- <b>Уверенность:</b>  
Если не видишь подходящих условий для входа, ответь только так: Нет сигнала.


Свечи (таймфреймы):
{candle_text}

Дата и время UTC: {now}
"""
    return prompt.strip()