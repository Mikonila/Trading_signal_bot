from datetime import datetime
import json

def build_multi_tf_prompt(candles_by_tf: dict) -> str:
    now = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ")

    prompt = f"""
Ты — профессиональный криптоаналитик. Проанализируй BTC/USDT на 1h, 1d, 1w таймфреймах.

🔹 Подсказки:
EMA50 > EMA200 на D1 → глобальный восходящий тренд.
EMA50 < EMA200 на D1 → глобальный нисходящий тренд.
На вход в сделку идём только если тренды 1D и H1 совпадают.
H1 используется для поиска оптимальной точки входа.

🔹 RSI
RSI > 70 (на D1 или H4) → не открывать LONG, рынок перегрет.
RSI < 30 (на D1 или H4) → не открывать SHORT, рынок перепродан.
RSI на H1 помогает найти локальные развороты внутри глобального тренда.

🔹 Объём:
Резкое увеличение объёма на H1 или D1 может указывать на сильный импульс.
Если объём резко падает — вероятна консолидация или завершение тренда.

🔹 MACD Histogram:
растёт вместе с трендом → вход сильнее.
снижается при сохранении тренда → возможна коррекция.

🔹 SL: за ближайший swing high/low на H4 или чуть ниже/выше EMA200.
🔹 TP: 2–5% или ближайший уровень сопротивления/поддержки на D1.

Если импульс сильный (MACD Histogram растёт) → держать 1–3 дня.
Если сигнал средний → держать 2–10 часов.
Если сигнал слабый → не входить.
Если график во флэте (M15 < 0.2%) → не входить.
Тейк-профит должен быть не менее 2х стоп-лосса (RR ≥ 2) и не менее 1% от цены. Если не выполняется — не входить.


🔹 Формат вывода строго в HTML (только <b>, <i>, <a>, <code>):

<b>Сигнал:</b> Покупать/Продавать
<b>Цена BTC/USDT:</b> 115200  
<b>SL:</b> 114800 / <b>TP:</b> 115900  
<b>Комментарий:</b> H1 и D1 в восходящем тренде. RSI и MACD подтверждают.  
<b>Держать:</b> 2 дня  
<b>Уверенность:</b> 82%

❗️Заголовки должны быть точно такими же:
- <b>Сигнал:</b>  
- <b>Цена BTC/USDT:</b>  
- <b>SL:</b> … / <b>TP:</b> …  
- <b>Комментарий:</b>  
- <b>Держать:</b>  
- <b>Уверенность:</b>  

Если не видишь подходящих условий для входа, ответь только так: Нет сигнала.

Свечи (таймфреймы):
"""

    # вставка свечей
    for tf, candles in candles_by_tf.items():
        prompt += f"\n📊 Свечи {tf} (последние {len(candles)}):\n"
        prompt += json.dumps(candles, indent=2)

    prompt += f"\n\nДата и время UTC: {now}"

    return prompt.strip()
