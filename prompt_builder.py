from datetime import datetime

def build_prompt(ind: dict) -> str:
    now_utc = datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%SZ")


    return f"""
Ты — профессиональный криптоаналитик. Проанализируй BTC/USDT на 1-часовом таймфрейме.

🔹 Используй значения индикаторов:  
- Цена: ${ind['price']}  
- EMA-50: ${ind['ema50']}  
- EMA-200: ${ind['ema200']}  
- RSI(14): {ind['rsi']}  
- MACD: {ind['macd']}  
- MACD Signal: {ind['macd_signal']}  
- MACD Histogram: {ind['macd_hist']}
- Объём (H1): {ind.get('volume', '—')}


🔹 Дай краткий вывод для трейдера: покупать / продавать / держать.


🔹 Подсказки для бота (таймфреймы H1, H4, D1)
🔹 Тренд:
EMA50 > EMA200 на 1D → глобальный восходящий тренд.
EMA50 < EMA200 на 1D → глобальный нисходящий тренд.
На вход в сделку идём только если тренды 1D и H4 совпадают.
H1 используется для поиска оптимальной точки входа.

🔹 RSI
RSI > 70 (на D1 или H4) → не открывать LONG, рынок перегрет.
RSI < 30 (на D1 или H4) → не открывать SHORT, рынок перепродан.
RSI на H1 помогает найти локальные развороты внутри глобального тренда.

🔹 MACD и Signal на D1 и H4 должны подтверждать основной тренд.
🔹 MACD Histogram:
растёт вместе с трендом → вход сильнее, выше вероятность продолжения.
снижается при сохранении тренда → вход возможен, но осторожно (вероятность коррекции).

🔹 Stop-Loss: за ближайший swing high/low на H4 или чуть ниже/выше EMA200.
🔹 Take-Profit: 2–5% или по ближайшему сильному уровню сопротивления/поддержки (на D1).
Можно использовать частичную фиксацию (50% позиции на +2%, остальное по тренду).

🔹 Удержание позиции
Сильный импульс (рост объёмов, MACD Histogram усиливается на D1/H4) → держать до 4–5 дней.
Умеренный сигнал (только EMA+RSI подтверждают, без объёмов) → держать 0.5–2 дня.

🔹 Объёмы:
Рост объёмов при росте цены подтверждает силу тренда.
Если объёмы падают на росте цены — тренд может быть слабым.


🔹 Уверенность (%) рассчитывать по совпадению индикаторов:
Например:
3 подтверждения (EMA, RSI, MACD) → 80–90%.
2 подтверждения → 60–70%.
1 подтверждение → сигнал не выдаётся.

🔹 Формат вывода строго в HTML (только поддерживаемые Telegram теги: <b>, <i>, <code>, <a>). Пример вывода:

<b>Сигнал:</b> Покупать 
<b>Цена BTC/USDT:</b> 115200   
<b>SL:</b> 114800 / <b>TP:</b> 115900  
<b>Комментарий:</b> На M1 и H1 наблюдается восходящий тренд. EMA и MACD подтверждают бычью динамику.  
<b>Держать:</b> 20 минут  
<b>Уверенность:</b> 75%

❗️ Заголовки должны быть всегда точно такими же (включая пробелы и знаки препинания):
- <b>Сигнал:</b>  
- <b>Цена BTC/USDT:</b>  
- <b>SL:</b> … / <b>TP:</b> …  
- <b>Комментарий:</b>  
- <b>Держать:</b>  
- <b>Уверенность:</b>  


Текущая цена BTC/USDT: ${ind['price']}
Дата и время (UTC): {now_utc}
"""
